package org.example.operators.java;

public class UnaryOperator {
    public static void main(String[] args) {
        int a = -10;
        System.out.println(a);
        int b = +10;
        System.out.println(b);
        ++b;
        System.out.println(b);
        --b;
        System.out.println(b);

    }
}

